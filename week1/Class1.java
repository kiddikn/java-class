public class Class1 {
    public static void main(String[] args) {
        System.out.println("2と3を足した結果は" + (2+3) + "です。");
        System.out.println("2から3を引いた結果は" +(2-3) + "です。");
        System.out.println("2と3をかけた結果は" + (2*3)+ "です。");
        System.out.println("2を3で割った商は" + (2/3) + "です。");
        System.out.println("2を3で割った剰余は" +(2%3) + "です。");
    }
}
